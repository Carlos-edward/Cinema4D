    public void systemCadeira(JLabel cadeira,int cadeiraOcupada){
          
        
         if(contador1 > 0 && ocupado[cadeiraOcupada] < 1){
            cadeira.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icons/inteira.png")));
            contador1 -=1;
            jProgressBar1.setValue(jProgressBar1.getValue()+1);
            inteiro +=1; 
            verifClick += 1; 
        }else if(contador2 > 0 && ocupado[cadeiraOcupada] <1){
            cadeira.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icons/Meia.png")));
            contador2 -=1;
            jProgressBar1.setValue(jProgressBar1.getValue()+1);
            meio += 1;
            verifClick += 1;   
        } else if(ocupado[cadeiraOcupada] == 1){
            JOptionPane.showMessageDialog(QuantidadeDeIngresso,"️CADEIRA OCUPADA!️","⚠ WARNINIG ⚠",WARNING_MESSAGE);  
        }else{
            JOptionPane.showMessageDialog(QuantidadeDeIngresso,"️ ATENÇÃO VOCÊ JÁ GASTOU TODO SEUS INGRESSO!️","⚠ WARNINIG ⚠",WARNING_MESSAGE);
        }
         
        if(jProgressBar1.getValue() == 50){
            jProgressBar1.setForeground(new java.awt.Color(255,255,0));

        }else if(jProgressBar1.getValue() == 100){
              jProgressBar1.setForeground(new java.awt.Color(255,0,0));
              JOptionPane.showMessageDialog(QuantidadeDeIngresso,"️SALA CHEIA!️","⚠ WARNINIG ⚠",WARNING_MESSAGE);
        
        }
          
        ocupado[cadeiraOcupada] = 1;
        String valueInteiroFilme = String.valueOf(contador1); 
        inteirosFilme.setText(valueInteiroFilme);
        String meioInteiroFilme = String.valueOf(contador2); 
        meioFilme.setText(meioInteiroFilme);
        

    }

  
    
